export default {
    //用户名正则，4到16位（字母，数字，下划线，减号）
    regExpUsername:/^[a-zA-Z0-9_-]{4,16}$/,
    // 密碼8到16位（字母，数字，下划线，减号）任意组合
    regExpPassword:/^[a-zA-Z0-9_-]{8,16}$/,
    //Email正则
    regExpEmail:/^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/,
    // 手机号码正则
    regExpPhone:/^1(3|4|5|7|8|9)[0-9]\d{8}$/,
    // 固定电话正则
    regExpTel:/\d{3}-\d{8}|\d{4}-\d{7}/,
    //身份证号（18位）正则
    regExpIdCard:/^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$/,
    //ipv4地址正则
    regExpIp:/^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/,
    //包含中文正则
    regExpChinese:/[\u4E00-\u9FA5]/,
    // 日期验证
    regExpDate:/^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$/,
    //URL正则
    regExpUrl:/^((https?|ftp|file):\/\/)?([\da-z\.-]+)\.([a-z\.]{2,6})([\/\w \.-]*)*\/?$/,
    // 非零正整数
    regExpInteger:/^\+?[1-9][0-9]*$/,
};